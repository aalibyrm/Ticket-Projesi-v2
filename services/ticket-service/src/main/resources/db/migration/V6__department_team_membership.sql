CREATE TABLE IF NOT EXISTS departments (
  id UUID PRIMARY KEY,
  code VARCHAR(80) NOT NULL UNIQUE,
  name VARCHAR(160) NOT NULL,
  active BOOLEAN NOT NULL DEFAULT true,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS support_teams (
  id UUID PRIMARY KEY,
  department_id UUID NOT NULL REFERENCES departments(id),
  code VARCHAR(80) NOT NULL UNIQUE,
  name VARCHAR(160) NOT NULL,
  lead_actor_id UUID NOT NULL,
  active BOOLEAN NOT NULL DEFAULT true,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  CONSTRAINT support_teams_department_code_unique UNIQUE (department_id, code)
);

CREATE INDEX IF NOT EXISTS idx_support_teams_department ON support_teams (department_id);
CREATE INDEX IF NOT EXISTS idx_support_teams_active ON support_teams (active);

CREATE TABLE IF NOT EXISTS team_members (
  id UUID PRIMARY KEY,
  team_id UUID NOT NULL REFERENCES support_teams(id),
  actor_id UUID NOT NULL,
  team_lead BOOLEAN NOT NULL DEFAULT false,
  active BOOLEAN NOT NULL DEFAULT true,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  CONSTRAINT team_members_team_actor_unique UNIQUE (team_id, actor_id)
);

CREATE INDEX IF NOT EXISTS idx_team_members_actor ON team_members (actor_id);
CREATE INDEX IF NOT EXISTS idx_team_members_team_active ON team_members (team_id, active);

CREATE UNIQUE INDEX IF NOT EXISTS idx_team_members_one_active_lead
  ON team_members (team_id)
  WHERE team_lead = true AND active = true;

